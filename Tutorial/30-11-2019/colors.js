function setup() {
    createCanvas(400, 300);


}

function draw() {
    background(200);
    line(0, 0, 400, 400);
    fill(255, 0, 0, 175)
    noStroke();
    ellipse(100, 80, 70, 70);


    fill(0, 255, 0);
    stroke(0, 0, 255, );
    strokeWeight(4);
    rectMode(CENTER);
    rect(200, 150, 50, 70);




}